// Vetores (conhecidos como Arrays) permitem armazenar uma lista dentro de uma variável.
// Analogia: Um armário onde cada porta tem um número (índice) e guarda uma informação.

let nomePessoas = ["Gabriela", "Fernanda", "João", "Thiago"];

// Consultar todas as informações do armário (vetor)
console.log(nomePessoas);

// Consulta indicando a posição do item:
// OBS: As posições SEMPRE começam no zero.
console.log(nomePessoas[0]); // Primeira opção: "Gabriela"

// Para consultar o segundo item, chama-se o índice 1, e assim por diante.
console.log(nomePessoas[1]); // Segunda opção: "Fernanda"