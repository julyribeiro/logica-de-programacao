// JSON (JavaScript Object Notation)
// Protocolo universal para troca de dados entre sistemas.
// Agrupamento de dados de forma mais organizada

// Regras importantes:
// As chaves ficam entre aspas duplas " "
// Usa " {} " para objeto
// Usa " : " para separar chave e valor
// Usa " , " para separar itens

// Por que usar JSON em vez de variáveis soltas?
// Exemplo "complicado" (sem JSON):
let name = "July";
let products = ["Mouse", "Teclado"];
let values = [29.90, 129.99];

