// Operadores de Atribuição Abreviada
// Atalhos para: variável = variável + valor

let preco1 = 10;
preco1 += 5; // ADIÇÃO: Mesmo que 10 + 5
console.log("Atribuição com soma: " + preco1);

let preco2 = 10;
preco2 -= 5; // SUBTRAÇÃO: Mesmo que 10 - 5
console.log("Atribuição com subtração: " + preco2);

let preco3 = 10;
preco3 *= 5; // MULTIPLICAÇÃO: Mesmo que 10 * 5
console.log("Atribuição com multiplicação: " + preco3);

let preco4 = 10;
preco4 /= 5; // DIVISÃO: Mesmo que 10 / 5
console.log("Atribuição com divisão: " + preco4);

let preco5 = 10; // Adicionada a declaração
preco5 %= 5; // RESTO: Mesmo que 10 % 5
console.log("Atribuição com resto: " + preco5);