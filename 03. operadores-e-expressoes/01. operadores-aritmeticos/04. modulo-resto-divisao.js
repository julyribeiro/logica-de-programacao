// Divisão comum (/)
let calculoDivisao = 10 / 3; 
console.log("Resultado da divisão: " + calculoDivisao.toFixed(2)); // Saída: 3.33

// Resto da Divisão (%) -> O que sobra quando a divisão não é exata
// Útil para descobrir se um número é Par ou Ímpar!
let calculoRestoDivisao = 10 % 3; 

console.log("O que sobra dessa divisão (Resto): " + calculoRestoDivisao); // Saída: 1