let contador = 1;
console.log("Início: " + contador);

// '++' Incremento: Adiciona +1 ao valor atual
contador++; // Agora é 2
contador++; // Agora é 3
contador++; // Agora é 4

console.log("Após incrementos: " + contador);

// '--' Decremento: Subtrai -1 do valor atual
contador--; // Volta para 3
contador--; // Volta para 2

console.log("Após decrementos: " + contador);